package com.rz.iot.bpo.mina.model;

import java.util.Date;

public class MinaBatch {
    //
    private Integer id;

    //批次名称
    private String batchName;

    //排班规则id
    private Integer scheduleRuleId;

    //类型
    private Integer type;

    //状态
    private Integer status;

    //创建时间
    private Date createTime;

    //修改时间
    private Date updateTime;

    public Integer getId() {
        return id;
    }

    public void setId(Integer id) {
        this.id = id;
    }

    public String getBatchName() {
        return batchName;
    }

    public void setBatchName(String batchName) {
        this.batchName = batchName == null ? null : batchName.trim();
    }

    public Integer getScheduleRuleId() {
        return scheduleRuleId;
    }

    public void setScheduleRuleId(Integer scheduleRuleId) {
        this.scheduleRuleId = scheduleRuleId;
    }

    public Integer getType() {
        return type;
    }

    public void setType(Integer type) {
        this.type = type;
    }

    public Integer getStatus() {
        return status;
    }

    public void setStatus(Integer status) {
        this.status = status;
    }

    public Date getCreateTime() {
        return createTime;
    }

    public void setCreateTime(Date createTime) {
        this.createTime = createTime;
    }

    public Date getUpdateTime() {
        return updateTime;
    }

    public void setUpdateTime(Date updateTime) {
        this.updateTime = updateTime;
    }
}